    {
        $user = User::with(['topics','posts'])->find($id);

        $profileImageUrl = $this->getProfileImageUrl($user->photourl);

        return view('profiles.show', [
            'user' => $user,
            'profileImageUrl' => $profileImageUrl
        ]);
