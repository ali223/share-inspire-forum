    protected function getProfileImageUrl($imageFile)
    {
        if (Storage::disk('dropbox')->exists($imageFile)) {
                return Storage::disk('dropbox')
                             ->getAdapter()
                             ->getTemporaryLink($imageFile);
        }

        return null;
    }
